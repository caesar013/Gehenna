'''
You can use Python to operate with numbers. To output the result of a calculation, you just need to enter the mathematical operation right into the print statement.
Just like in math we use + and - for addition and subtraction, respectively.
'''
print(9 + 4 - 7)

# Multiplication is done using an asterisk symbol *.

print(12 * 12)

# Division is done using a slash /

print(8 / 2)

# Using a slash to divide numbers always produces a decimal number.

